int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	dif;

	dif = 0;
	i = 0;
	while (dif == 0)
	{
		dif = s1[i] - s2[i];
		if (s1[i] == '\0' || s2[i] == '\0')
			break ;
		i++;
	}
	return (dif);
}
